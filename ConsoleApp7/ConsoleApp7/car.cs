class car
{
    public string model;
    public int topspeed;
    public double engine;
    public double km;
    public int hp;
  

    public car(string model, int topspeed, double engine, double km, int hp)
    {
        this.model = model;
        this.topspeed = topspeed;
        this.engine = engine;
        this.km = km;
        this.hp = hp;
    }
  
}
